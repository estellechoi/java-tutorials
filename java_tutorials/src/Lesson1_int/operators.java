package Lesson1_int;

public class operators {

	public static void main(String[] args) {
		// operators 연산자
		
		int result=1+2;
		System.out.println(result);
		
		result=result-1;
		System.out.println(result);
		
		result=result*2;
		System.out.println(result);
		
		result=result/2;
		System.out.println(result);
		
		result=result+8;
		System.out.println(result);
		
		result=result%7;              // 7로 나누었을 때의 나머지 값
		System.out.println(result);

	}

}
